export function downloadFile(content: string, filename: string, mimeType: string) {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

export function exportAndDownload(
  data: AnalystFinding[],
  options: ExportOptions
) {
  const date = new Date().toISOString().split("T")[0];
  const baseName = `quantivis-${options.title.toLowerCase().replace(/\s+/g, "-")}-${date}`;
  
  switch (options.format) {
    case "markdown": {
      const content = exportInsightsAsMarkdown(data, options);
      downloadFile(content, `${baseName}.md`, "text/markdown");
      break;
    }
    case "csv": {
      const content = exportInsightsAsCSV(data);
      downloadFile(content, `${baseName}.csv`, "text/csv");
      break;
    }
    case "json": {
      const content = JSON.stringify({
        title: options.title,
        generated_at: date,
        organization: options.organizationName,
        dataset: options.datasetName,
        findings: data,
      }, null, 2);
      downloadFile(content, `${baseName}.json`, "application/json");
      break;
    }
  }
}
